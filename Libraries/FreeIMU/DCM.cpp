//Set sensor values received from FreeIMU library
//
void DCM::setSensorVals(float * cal_val)
{
  for(int i = 0; i < 3; i++) {
	accel[i] = cal_val[i];
	gyro[i] = cal_val[i + 3];
	magnetom[i] = cal_val[i + 6];
	MAG_Heading = TO_RAD(cal_val[9]);
  }
}


//
//DCM to Quaternion conversion based on Vector Nav Application Note
//AN002, Quaternion Math
// 
void DCM::getDCM2Q(float * q)
{
  //quaternions as defined in Vector Nav App Note AN002
  q3 = 0.5 * sqrt(DCM_Matrix[0][0]+DCM_Matrix[1][1]+DCM_Matrix[2][2]+1);
  q0 = (DCM_Matrix[1][2]-DCM_Matrix[2][1])/(4*q3);
  q1 = (DCM_Matrix[2][0]-DCM_Matrix[0][2])/(4*q3);
  q2 = (DCM_Matrix[0][1]-DCM_Matrix[1][0])/(4*q3);

  //convert to quaternion notation as used in the FreeIMU library
  q[0] =  q3;
  q[1] = -q0;
  q[2] = -q1;
  q[3] = -q2;

}

void DCM::calDCM() 
{
    Matrix_update();
    Normalize();
    Drift_correction();
}


